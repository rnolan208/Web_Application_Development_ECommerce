function getPaymentHTML(method) {
    switch (method) {
        case "credit-card":
        case "stripe":
            return `
                <div id="credit-card-stripe-fields" class="payment-fields">
                    <div class="row g-3 mb-3">
                        <div class="col-md-6">
                            <input class="form-control" placeholder="Name on Card">
                        </div>
                        <div class="col-md-3">
                            <input class="form-control" placeholder="MM/YY">
                        </div>
                        <div class="col-md-3">
                            <input class="form-control" placeholder="CVV">
                        </div>
                        <div class="col-12">
                            <input class="form-control" placeholder="Card Number">
                        </div>
                    </div>
                </div>`;
        case "paypal":
            return `
                <div id="paypal-fields" class="payment-fields">
                    <div class="row g-3">
                        <div class="col-12">
                            <input class="form-control" placeholder="PayPal Email:">
                        </div>
                    </div>
                </div>`;
        case "crypto":
            return `
                <div id="crypto-fields" class="payment-fields">
                    <div class="row g-3 mb-3 align-items-center">
                        <div class="col-md-2">
                            <label>Select cryptocurrency:</label>
                        </div>
                        <div class="col-md-4">
                            <select class="form-control">
                                <option value="bitcoin">Bitcoin</option>
                                <option value="ethereum">Ethereum</option>
                                <option value="litecoin">Litecoin</option>
                            </select>
                        </div>
                    </div>
                    <div class="row g-3">
                        <div class="col-12">
                            <input class="form-control" placeholder="Account Number">
                        </div>
                    </div>
                </div>`;
        default:
            return "";
    }
}

// export module
module.exports = { getPaymentHTML };